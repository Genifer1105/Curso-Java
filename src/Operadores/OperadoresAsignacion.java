public class OperadoresAsignacion {
    
     public static void main(String args[]) {
         
         // Se convinan los operadores para abreviar operaciones sobre el mismo número
         
         int numero = 10;
         
         numero = numero + 5;
         numero += 5; // Convinar operador de suma más operador de asignación
          
         numero = numero - 1;    
         numero -= 1; // *=, /= 
         
     }
}
