/*
Do while, primero se ejecutan las instrucciones almenos una vez y
después comprueba si la condición es verdadera
 */
package Ciclos;

/**
 *
 * @author genifer.moreno
 */
public class CicloDoWhile {
    
    public static void main(String[] args) {
        
        int i=1; // 11
        
        do{
            System.out.println(i);
            i++;
        } while (i<=10);  // muestra el 11 y no aumenta ni nada
        
        

    }
}
